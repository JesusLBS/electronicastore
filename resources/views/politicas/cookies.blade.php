<h4><b>¿Cuánto tiempo permanecerán las <i>cookies</i> en mi dispositivo?</b></h4>
<p>El tiempo que una <i>cookie</i> permanecerá en su dispositivo lo determinará la naturaleza de la misma, que puede ser &quot;permanente&quot; o de &quot;sesión&quot;.<br>
